    FillImage(image, row, col-1);
    FillImage(image, row+1, col);
    FillImage(image, row, col+1);
}
}

double fact (double n){
    if(n==1 || n ==0 ){
        return 1;
    }
    else return n*fact(n-1);
}

int Fibonacci (int n){
    if( n==1 || n ==2 ){
        return 1;
    }
    else return Fibonacci(n-1) + Fibonacci(n-2);
}

